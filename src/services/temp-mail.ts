export function generateDisplayName(): string {
  // Prefer simple ASCII names — accents/spaces sometimes trip Qwen validation.
  const names = [
    "AnaSilva",
    "BrunoCosta",
    "CarlaMendes",
    "DiegoRocha",
    "ElenaSouza",
    "FelipeLima",
    "GabrielaNunes",
    "HenriqueAlves",
  ];
  const base = names[Math.floor(Math.random() * names.length)];
  return `${base}${Math.random().toString(36).slice(2, 5)}`;
}
